#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*array;
	int		i;

	i = 0;
	array = malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (array == NULL)
		return (NULL);
	while (s1[i])
	{
		array[i] = s1[i];
		i++;
	}
	array[i] = '\0';
	return (array);
}
